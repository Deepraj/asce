<h2><strong><a id="asce_7_standard"></a>ASCE 7 Standard</strong></h2>
<p><strong>ASCE/SEI 7</strong> <em><a href="http://www.asce.org/structural-engineering/asce-7-and-sei-standards/" target="_blank" rel="noopener">Minimum Design Loads and Associated Criteria for Buildings and Other Structures</a></em> is an integral part of building codes in the United States. Many of the provisions are adopted by reference in the ICC <em>International Building Code</em> and the NFPA 5000 <em>Building Construction and Safety Code</em>.</p>
<p>Structural engineers, architects, building officials, and those engaged in preparing and administering local building codes will find the structural load requirements essential to their practice.</p>
<p>The information in the ASCE 7 Online Platform provides means for determining design loads specified in ASCE/SEI Standard 7-10, ASCE/SE Standard 7-16, and ASCE/SEI Standard 7-22</p>
<h2><strong><a id="what_is_asce_7_online"></a>What is ASCE 7 Online?</strong></h2>
