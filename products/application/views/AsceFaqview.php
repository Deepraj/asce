<h2><strong><a id="frequently_asked_questions"></a>Frequently Asked Questions (FAQ&rsquo;s)</strong></h2>
<h3><strong>General Questions</strong></h3>
<ol style="
    margin-left: 1em;
">
<li><strong>What is the ASCE 7 Online Dashboard?<br /> </strong>The ASCE 7 Online Dashboard is an easy way for the user to navigate between features. Located at the top right-hand corner of the screen, feature icons include bookshelf, notes, bookmarks, search, print, history and account.</li>
<li><strong>What is the Bookshelf feature?<br /> </strong>The Bookshelf feature shows your books with an active license.</li>
<li><strong>How do I view the redlining features? <br /> </strong>Click on the icon titled History at the top of your screen. View changes in current chapter or select all chapters, or view in current or previous editions. Decide the date range you want to search.</li>
<li><strong>How do I add a corporate or personal note?<br /> </strong>Click the icon titled Notes at the top of your screen. Highlight text where you want to put a note. When blue menu bar pops up, click on note tab and type in your note. Designate if the note is a corporate or personal note and save. <strong>(If you have an individual user license you will not be able to add a corporate note.)</strong> Only corporate licenses include corporate note taking feature. Corporate annotations are controlled by a single corporate administrator account. All notes will appear in the dashboard at the top of the screen in the Notes Tab. Bookmarks are handled the same way in the system.</li>
<li><strong>How do I use the Advanced Search Feature?<br /> </strong>Click on the icon titled Search at the top of your screen. Type in the key words you want to search and indicate any additional parameters such as figure captions, references, tables with captions, table of contents, and appendices. Click on the result you wish to view.</li>
<li><strong>How do I print from ASCE 7 Online?<br /> </strong>Select the print icon and print by section or selected chapter. You can also copy and paste the text you want in another application, such as Word, and print that document. Please refer to the <a href="https://asce7.online/TermsOfUse">terms of use</a> for additional information.</li>
<li><strong>What is a corporate administrator in ASCE 7 Online?<br /> </strong>A corporate administer is someone designated by the institution or company to manage the corporate license account. Corporate administrators can view license information and manage corporate annotations.</li>
</ol>
